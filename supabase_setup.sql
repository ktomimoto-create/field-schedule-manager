-- 4. 物件マスタのダミーデータ（10,000件）をPL/pgSQLで生成して投入
CREATE OR REPLACE FUNCTION generate_dummy_properties() RETURNS void AS $$
DECLARE
  prefectures text[] := ARRAY['東京', '神奈川', '埼玉', '千葉', '長野'];
  cities text[] := ARRAY['世田谷区', '川崎市宮前区', 'さいたま市大宮区', '船橋市', '松本市'];
  models text[] := ARRAY['Type-A', 'Type-B', 'Type-C', 'Type-D'];
  names text[] := ARRAY['エル・コモド', 'ドレッセ', 'サンライズ', 'グリーンヒル', 'メゾン・ド', 'パークハイツ', 'コンフォート', 'グランディール', 'シャトー', 'ビューハイツ'];
  suffixes text[] := ARRAY['葛西', '下馬', '宮前平', '大宮', '松本', '船橋', '世田谷', '渋谷', '新宿', '横浜'];
  i integer;
  unitNum text;
  nameBase text;
  nameSuffix text;
  propName text;
  pref text;
  city text;
  addr text;
  boxCount integer;
  modelType text;
BEGIN
  FOR i IN 1..10000 LOOP
    unitNum := (100000 + i)::text;
    nameBase := names[mod(i, array_length(names, 1)) + 1];
    nameSuffix := suffixes[mod((i + i / array_length(names, 1)), array_length(suffixes, 1)) + 1];
    propName := nameBase || nameSuffix || '第' || ceil(i / 100.0)::text || '棟';
    
    pref := prefectures[mod(i, array_length(prefectures, 1)) + 1];
    city := cities[mod(i, array_length(cities, 1)) + 1];
    addr := pref || city || ' ' || mod(i, 10)::text || '-' || mod(i, 3)::text;
    
    boxCount := 4 + mod(i, 16);
    modelType := models[mod(i, array_length(models, 1)) + 1];

    INSERT INTO properties (unit_number, property_name, address, box_count, model_type)
    VALUES (unitNum, propName, addr, boxCount, modelType);
  END LOOP;
END;
$$ LANGUAGE plpgsql;

