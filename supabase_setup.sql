-- 現場対応予定・行動管理システム Supabase セットアップ SQL
-- このスクリプトは Supabase の SQL Editor にコピー＆ペーストして実行してください。

-- 既存のテーブルがある場合は削除（注意：データは消去されます）
DROP TABLE IF EXISTS audit_logs CASCADE;
DROP TABLE IF EXISTS schedules CASCADE;
DROP TABLE IF EXISTS work_types CASCADE;
DROP TABLE IF EXISTS staff CASCADE;
DROP TABLE IF EXISTS properties CASCADE;

-- 1. 物件マスタテーブルの作成
CREATE TABLE properties (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  unit_number TEXT NOT NULL,
  property_name TEXT NOT NULL,
  address TEXT,
  box_count INTEGER,
  model_type TEXT,
  created_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP
);

CREATE INDEX idx_properties_unit_number ON properties(unit_number);
CREATE INDEX idx_properties_property_name ON properties(property_name);

-- 2. スタッフ（対応者）テーブルの作成
CREATE TABLE staff (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name TEXT NOT NULL,
  email TEXT UNIQUE,
  default_course TEXT,
  is_active INTEGER DEFAULT 1,
  role TEXT DEFAULT 'user',
  created_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP
);

-- 3. 予定項目（種別）マスタテーブルの作成
CREATE TABLE work_types (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name TEXT NOT NULL UNIQUE,
  is_internal INTEGER DEFAULT 1,
  sort_order INTEGER DEFAULT 0,
  created_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP
);

-- 4. スケジュール（予定）テーブルの作成
CREATE TABLE schedules (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  status TEXT NOT NULL, -- 'draft' (仮), 'confirmed' (確定), 'cancelled' (キャンセル)
  division TEXT, -- 区分
  type TEXT, -- タイプ
  box TEXT, -- BOX
  unit_number TEXT, -- 号機
  property_name TEXT NOT NULL, -- 物件名
  work_type TEXT, -- 種別 (フリー、定期、工事、保守、障害対応など)
  description TEXT, -- 作業内容
  target_time TEXT, -- 時間
  date TEXT NOT NULL, -- YYYY-MM-DD
  staff_id BIGINT REFERENCES staff(id) ON DELETE SET NULL, -- 対応者
  staff_name TEXT, -- 対応者名（非正規化/表示用）
  area TEXT, -- エリア
  prefecture TEXT, -- 県別
  transport TEXT, -- 移動
  co_worker TEXT, -- 同行者
  request_number TEXT, -- 依頼番号
  time_limit TEXT, -- TIME
  course TEXT, -- コース
  result TEXT, -- 結果 (空欄 または '完了' など)
  started_at TEXT, -- 開始時刻 (HH:MM)
  completed_at TEXT, -- 完了時刻 (HH:MM)
  notes TEXT, -- 備考
  report_notes TEXT, -- 対応報告メモ
  disorder_type TEXT, -- 障害区分
  level TEXT, -- level
  level_3 TEXT, -- level 3
  sort_order INTEGER DEFAULT 0,
  is_transferred INTEGER DEFAULT 0, -- 行動予定表へ移行済みフラグ
  created_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP
);

CREATE INDEX idx_schedules_date ON schedules(date);
CREATE INDEX idx_schedules_staff_id ON schedules(staff_id);

-- 5. 操作履歴テーブルの作成
CREATE TABLE audit_logs (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  schedule_id BIGINT,
  action TEXT NOT NULL,
  changed_by TEXT NOT NULL,
  property_name TEXT,
  details TEXT,
  timestamp TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP
);

CREATE INDEX idx_audit_logs_timestamp ON audit_logs(timestamp);
CREATE INDEX idx_audit_logs_schedule_id ON audit_logs(schedule_id);


